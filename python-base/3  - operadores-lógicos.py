'''Operadores Lógicos
Estes operadores retornam como respostas apenas duas possibilidades:

True False São eles: Maior, maior igual, menor igual, igualdade e diferente

'''

# Maior que (>)

8 > 4
# resultado: True


8 > 8
# resultado: False


10 < 5
# resultado: False


# Maior igual (>=)

8 > 4
# resultado: True


8 >= 8
# resultado: True


# Menor que (<)

10 < 20
# resultado: True

10 < 10
# resultado: False

20 < 21
# resultado:True


# Menor igual (<=)

20 <= 20
# resultado: True

#igualdade (==)

5 == 5
# resultado:True

5 == 6
# resultado: False


# Diferente (!=)

5 != 6
# resultado: True


5 != 5
# resultado: False